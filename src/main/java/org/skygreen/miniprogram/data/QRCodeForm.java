package org.skygreen.miniprogram.data;

public class QRCodeForm {

  private String path;

  private String scene;

  public QRCodeForm() {}

  public String getPath() {
    return path;
  }

  public void setPath(String path) {
    this.path = path;
  }

  public String getScene() {
    return scene;
  }

  public void setScene(String scene) {
    this.scene = scene;
  }

}
